package org.caltoopia.analysis.trace;

import java.util.Collection;

public interface CpuAffinity<Task,CPU> {

	/**
	 * @return the collection of "CPU:s" used as "affinity" for some "task"
	 */
	Collection<CPU> getCpus();
		
	/**
	 * @param task
	 * @return the "CPU" with which the given task has affinity (null if no such CPU, i.e. dynamic processor allocation)
	 */
	CPU getAffinity(Task task);
	
	/**
	 * @param task
	 * @param cpu
	 * Sets the affinity of the task to the given CPU. A null CPU means removing affinity (i.e. dynamic CPU allocation)
	 */
	void setAffinity(Task task, CPU cpu);
}
